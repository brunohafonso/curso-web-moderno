// para declarar variaveis em javascript usamos a palavra reservada var
var nomeDaVariavel = "valor da variavel";

// podemos utilizar a palavra reservada let também, que foi introduzido nas novas versões
// é recomendado utilizar o let por ser mais moderno
let nome_da_variavel = "valor da variavel";

// para declarar constantes usamos a palavra reservada const
// seu valor não pode ser alterado
const NOMEDACONSTANTE = "valor da constante";